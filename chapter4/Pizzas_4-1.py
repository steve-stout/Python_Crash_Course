#This is a simple loop program naming off pizzas.
pizzas = ['pepperoni', 'sausage', 'cheese', 'combo', 'pinapple']

#Print the names of all the pizzas
for pizza in pizzas:
    print(pizza)

print("\n")

#Print a sentence about each pizza
for pizza in pizzas:
    print(f"I really love {pizza} pizza!")

print("\nI really love pizza!")